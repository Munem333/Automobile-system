export function partPhotoUrl(category: string, index: number, slug?: string): string {
  if (slug && PART_PHOTOS[slug]) return resolveCatalogImage(PART_PHOTOS[slug]);
  const photos = PART_CATEGORY_PHOTOS[category] || PART_CATEGORY_PHOTOS.engine;
  const ref = photos[index % photos.length];
  if (/^\d+$/.test(ref)) return pexelsPhoto(Number(ref));
  return resolveCatalogImage(ref);
}

export function carPhotoUrl(brandSlug: string, modelSlug: string): string {
  const key = `${brandSlug}-${modelSlug}`;
  const ref = CAR_MODEL_PHOTOS[key] || 'unsplash:1492144534655-ae79c964c9d7';
  return resolveCatalogImage(ref);
}
