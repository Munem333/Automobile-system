/** 4K image URLs via Unsplash (3840×2160, q=90) */
export function img4k(photoId: string): string {
  const id = photoId.startsWith('photo-') ? photoId : `photo-${photoId}`;
  return `https://images.unsplash.com/${id}?w=3840&h=2160&fit=crop&q=90&auto=format`;
}

/** High-quality Pexels automotive / parts photos */
export function pexelsPhoto(id: number): string {
  return `https://images.pexels.com/photos/${id}/pexels-photo-${id}.jpeg?auto=compress&cs=tinysrgb&w=1920&h=1080&fit=crop&q=85`;
}

/** Resolve `pexels:123`, `unsplash:abc-def`, raw unsplash id, or full URL */
export function resolveCatalogImage(ref: string): string {
  if (ref.startsWith('http')) return ref;
  if (ref.startsWith('pexels:')) return pexelsPhoto(Number(ref.slice(7)));
  if (ref.startsWith('unsplash:')) return img4k(ref.slice(9));
  return img4k(ref);
}

